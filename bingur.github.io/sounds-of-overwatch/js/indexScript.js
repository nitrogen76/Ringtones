var heroIcon = $('.icon');
heroIcon.on('mouseover', function() {
  $(this).css({
    'border': '2px solid #01bffe',
    'box-shadow': '#01bffe 0px 0px 20px'
  });
});
heroIcon.on('mouseout', function() {
  $(this).css({
    'border': '2px solid #4d4d4d',
    'box-shadow': '#4d4d4d 0px 0px 20px'
  });
});

